package review;

import java.util.Scanner;

/*
 * 코인노래방 프로그램 만들기
 * 사용자에게 금액을 입력받고 부를 수 있는 노래 곡 수와 잔돈을 출력
 * 한 곡 : 300원
 * 
 * 입력 예시 
 * 금액 입력 : 1000원
 * 출력 예시
 * 3곡을 부를 수 있으며 잔돈은 100원 입니다.
 */
public class Review {
	/*
	 * 1. 변수 만들기( int money/count change)
	 * 2. 출력한다. (금액 입력)
	 * 3. 입력 받아서 money에 저장한다.
	 * 4. 연산한다.
	 * 	4-1. money를 300으로 나눠서 count에 저장한다.
	 * 	4-2. money를 300으로 나눈 나머지를 change에 저장한다.
	 * 5. 출력한다.
	 */
	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		int money = 0 , count = 0,change = 0;
		String msg = "금액 입력 : ";
		
		System.out.println(msg);
		money = sc.nextInt();
		count = money /300;
		change = money %300;
		
		System.out.println(count+"곡을 부를 수 있으며 잔돈은"+change+"원 입니다.");
	}

}













